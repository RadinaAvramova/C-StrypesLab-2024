#include <iostream>
using namespace std;

int main() {
    // Деклариране на променливи за координатите
    double x1, y1, x2, y2, slope;

    // Четене на координатите на двете точки
    cout << "Въведете координати на първата точка (x1, y1): ";
    cin >> x1 >> y1;
    cout << "Въведете координати на втората точка (x2, y2): ";
    cin >> x2 >> y2;

    // Проверка за деление на нула
    if (x2 - x1 == 0) {
        cout << "Правата е вертикална, наклонът е неопределен." << endl;
    } else {
        // Изчисляване на наклона
        slope = (y2 - y1) / (x2 - x1);
        // Отпечатване на резултата
        cout << "Наклонът на правата е: " << slope << endl;
    }

    return 0;
}
