#include <iostream>
using namespace std;

int main() {
    int a, b;

    // Въвеждане на стойностите на променливите
    cout << "Въведете стойността на a: ";
    cin >> a;
    cout << "Въведете стойността на b: ";
    cin >> b;

    // Размяна на стойностите без използване на трета променлива
    a = a + b;  // Стъпка 1: a става a + b
    b = a - b;  // Стъпка 2: b става (a + b) - b = a
    a = a - b;  // Стъпка 3: a става (a + b) - a = b

    // Отпечатване на новите стойности
    cout << "След размяната: a = " << a << ", b = " << b << endl;

    return 0;
}
